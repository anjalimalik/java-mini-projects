public class Temperature2 {
  public static void main(String[] args) {
    double fahrenheit;
    double celsius;
    celsius = 0;
    fahrenheit = ((celsius * 9) / 5) +32;
    System.out.println("Celsius: " + fahrenheit);
    System.out.println("Fahrenheit: " + celsius);
  }
}
